#include "parser.h"

int	token_check(char *str)
{
	if (str[0] == '|' && str[0 + 1] == '|')
		return (1);
	if (str[0] == '&' && str[0 + 1] == '&')
		return (2);
	if (str[0] == '|')
		return (3);
	if (str[0] == '(')
		return (4);
	if (str[0] == ')')
		return (5);
	if (str[0] == '<')
		return (6);
	if (str[0] == '>')
		return (7);
	return (0);
}